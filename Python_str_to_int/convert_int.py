



def convert_integer(user_num):                                       
                                                                    

    for i in range(len(user_num)):                                  
                                                                    

        if(user_num[i] == '1'):

            print ('A B', end = ' ')

        elif(user_num[i] == '2'):

            print('C D', end = ' ')

        elif(user_num[i] == '3'):

            print('E F', end = ' ')

        elif(user_num[i] == '4'):

            print('G H', end = ' ')

        elif(user_num[i] == '5'):

            print('I J', end = ' ')

        elif(user_num[i] == '6'):

            print('K L', end = ' ')

        elif(user_num[i] == '7'):

            print('M N', end = ' ')

        elif(user_num[i] == '8'):

            print('O P', end = ' ')

        elif(user_num[i] == '9'):

            print('Q R', end = ' ')

        elif(user_num[i] == '0'):

            print('F', end = ' ')

        else:

            print(' ', end = ' ')





